Write-Section "FIX 9: Docker -- start Redis container"
# ===========================================================================

try {
    $dockerInfo = docker info 2>&1
    $dockerOk   = ($LASTEXITCODE -eq 0)
} catch {
    $dockerOk = $false
}

if (-not $dockerOk) {
    Write-Skip "Docker not running" "start Docker Desktop manually if you need Redis"
} else {
    $containers   = docker ps --format "{{.Names}}" 2>&1
    $redisRunning = ($containers | Where-Object { $_ -like "*questionwork-redis*" }).Count -gt 0
    $allContainers = docker ps -a --format "{{.Names}}" 2>&1
    $redisExists  = ($allContainers | Where-Object { $_ -like "*questionwork-redis*" }).Count -gt 0

    if ($redisRunning) {
        Write-Skip "questionwork-redis container is already running"
    } elseif ($redisExists) {
        Write-Host "  [WARN] questionwork-redis exists but is stopped -- starting..." -ForegroundColor Yellow
        docker start questionwork-redis 2>&1 | Out-Null
        Start-Sleep -Seconds 2
        $nowRunning = (docker ps --format "{{.Names}}" 2>&1) -like "*questionwork-redis*"
        if ($nowRunning) {
            Write-Fixed "Started questionwork-redis container"
        } else {
            Write-Err "Failed to start questionwork-redis"
        }
    } else {
        Write-Host "  [WARN] questionwork-redis container not found -- creating..." -ForegroundColor Yellow
        docker run -d --name questionwork-redis -p 6379:6379 redis:7-alpine 2>&1 | Out-Null
        Start-Sleep -Seconds 3
        $nowRunning = (docker ps --format "{{.Names}}" 2>&1) -like "*questionwork-redis*"
        if ($nowRunning) {
            Write-Fixed "Created and started questionwork-redis (Redis 7 Alpine)"
        } else {
            Write-Err "Failed to create Redis container" "Check Docker and internet connection"
        }
    }
}

