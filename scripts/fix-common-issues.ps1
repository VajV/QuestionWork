Write-Section "FIX 8: Windows console UTF-8 encoding"
# ===========================================================================

$currentCP = [Console]::OutputEncoding.CodePage
if ($currentCP -ne 65001) {
    Write-Host "  [WARN] Console encoding is CP$currentCP (not UTF-8 / CP65001)" -ForegroundColor Yellow
    try {
        [Console]::OutputEncoding = [System.Text.Encoding]::UTF8
        $OutputEncoding           = [System.Text.Encoding]::UTF8
        chcp 65001 2>&1 | Out-Null
        Write-Fixed "Console encoding set to UTF-8 (CP65001)" "for this session only"
        Write-Info "To make permanent, add to PowerShell profile: [Console]::OutputEncoding = [System.Text.UTF8Encoding]::new()"
    } catch {
        Write-Err "Could not set UTF-8 encoding" $_.Exception.Message
    }
} else {
    Write-Skip "Console encoding is already UTF-8 (CP65001)"
}

