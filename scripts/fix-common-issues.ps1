Write-Section "FIX 3: Python virtual environment"
# ===========================================================================

$venvPath     = Join-Path $BACKEND_DIR ".venv"
$venvActivate = Join-Path $venvPath "Scripts\activate.ps1"
$venvPython   = Join-Path $venvPath "Scripts\python.exe"

if (Test-Path $venvPython) {
    Write-Skip "Python venv already exists" $venvPath
} else {
    Write-Host "  [WARN] Python venv not found -- creating..." -ForegroundColor Yellow

    # Check if Python is available
    $pythonCmd = $null
    foreach ($cmd in @("python", "python3", "py")) {
        try {
            $ver = & $cmd --version 2>&1
            if ($LASTEXITCODE -eq 0) {
                $pythonCmd = $cmd
                break
            }
        } catch {}
    }

    if ($pythonCmd) {
        Write-Info "Using Python: $pythonCmd"
        Push-Location $BACKEND_DIR
        try {
            & $pythonCmd -m venv .venv 2>&1 | Out-Null
            if (Test-Path $venvPython) {
                Write-Fixed "Python venv created" $venvPath
            } else {
                Write-Err "Venv creation failed" "Check Python installation"
            }
        } catch {
            Write-Err "Venv creation error" $_.Exception.Message
        }
        Pop-Location
    } else {
        Write-Err "Python not found in PATH" "Install Python 3.10+ from python.org"
    }
}

