# ─── Helpers ─────────────────────────────────────────────────────────────────

function Write-Section {
    param([string]$Title)
    Write-Host ""
    Write-Host "  +---------------------------------------------+" -ForegroundColor Cyan
    Write-Host ("  |  " + $Title.PadRight(43) + "|") -ForegroundColor Cyan
    Write-Host "  +---------------------------------------------+" -ForegroundColor Cyan
    Write-Host ""
}

function Write-Fixed {
    param([string]$Msg, [string]$Detail = "")
    $script:fixCount++
    Write-Host "  [FIXED] $Msg" -ForegroundColor Green -NoNewline
    if ($Detail) { Write-Host "  =>  $Detail" -ForegroundColor DarkGray } else { Write-Host "" }
}

function Write-Skip {
    param([string]$Msg, [string]$Detail = "")
    $script:skipCount++
    Write-Host "  [SKIP]  $Msg" -ForegroundColor Gray -NoNewline
    if ($Detail) { Write-Host "  =>  $Detail" -ForegroundColor DarkGray } else { Write-Host "" }
}

function Write-Err {
    param([string]$Msg, [string]$Detail = "")
    $script:failCount++
    Write-Host "  [ERROR] $Msg" -ForegroundColor Red -NoNewline
    if ($Detail) { Write-Host "  =>  $Detail" -ForegroundColor DarkGray } else { Write-Host "" }
}

function Write-Info {
    param([string]$Msg)
    Write-Host "    [INFO] $Msg" -ForegroundColor DarkGray
}

function Test-Port {
    param([int]$Port)
    $conn = Test-NetConnection -ComputerName 127.0.0.1 -Port $Port `
                -WarningAction SilentlyContinue -ErrorAction SilentlyContinue
    return $conn.TcpTestSucceeded
}

