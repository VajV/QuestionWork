# Validate existing .env has required keys
if (Test-Path $backendEnvPath) {
    $envContent = Get-Content $backendEnvPath -Raw -Encoding UTF8

    $requiredKeys = @("SECRET_KEY", "FRONTEND_URL", "JWT_ALGORITHM", "DATABASE_URL", "REDIS_URL")
    foreach ($key in $requiredKeys) {
        if ($envContent -notmatch "$key=") {
            Write-Host "  [WARN] backend\.env is missing key: $key" -ForegroundColor Yellow
            # Append missing key with default
            switch ($key) {
                "SECRET_KEY"    { Add-Content $backendEnvPath "`nSECRET_KEY=change-me-please-update-this" }
                "FRONTEND_URL"  { Add-Content $backendEnvPath "`nFRONTEND_URL=http://localhost:3000" }
                "JWT_ALGORITHM" { Add-Content $backendEnvPath "`nJWT_ALGORITHM=HS256" }
                "DATABASE_URL"  { Add-Content $backendEnvPath "`nDATABASE_URL=postgresql://postgres:postgres@localhost:5432/questionwork" }
                "REDIS_URL"     { Add-Content $backendEnvPath "`nREDIS_URL=redis://localhost:6379" }
            }
            Write-Fixed "Appended missing key to backend\.env" $key
        }
    }
}

