    if ($fastapiPath) {
        Write-Skip "Backend packages already installed" "fastapi found at $($fastapiPath.FullName)"
    } else {
        Write-Host "  [WARN] Backend packages not installed -- running pip install..." -ForegroundColor Yellow
        Push-Location $BACKEND_DIR
        try {
            & $venvPip install -r requirements.txt --quiet 2>&1
            if ($LASTEXITCODE -eq 0) {
                Write-Fixed "Backend packages installed" "pip install -r requirements.txt"
            } else {
                Write-Err "pip install failed" "Check requirements.txt and internet connection"
            }
        } catch {
            Write-Err "pip install error" $_.Exception.Message
        }
        Pop-Location
    }
}

